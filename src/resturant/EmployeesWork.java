    private void btn_equalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_equalActionPerformed
        String answer;;
        secondNum = Double.parseDouble(display_txt.getText());
        if (operation.equals("+")) {
            result = firstNum + secondNum;
            answer = String.format("%.2f", result);
            display_txt.setText(answer);
        } else if (operation.equals("-")) {
            result = firstNum - secondNum;
            answer = String.format("%.2f", result);
            display_txt.setText(answer);
        } else if (operation.equals("*")) {
            result = firstNum * secondNum;
            answer = String.format("%.2f", result);
            display_txt.setText(answer);
        } else if (operation.equals("/")) {
            result = firstNum / secondNum;
            answer = String.format("%.2f", result);
            display_txt.setText(answer);
        }
    }//GEN-LAST:event_btn_equalActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DefaultTableModel model = (DefaultTableModel) table.getModel();

        if (model.getRowCount() == 0) {
            JOptionPane.showMessageDialog(null, "حدد الفاتورة ..");
        } else {
            Calendar cal = new GregorianCalendar();
            int day = cal.get(Calendar.DAY_OF_MONTH);
            int month = cal.get(Calendar.MONTH);
            int year = cal.get(Calendar.YEAR);
            String date = "" + day + "/" + (month + 1) + "/" + year + "";

            SimpleDateFormat format = new SimpleDateFormat("EEEE");
            String day1 = format.format(new Date());

            format = new SimpleDateFormat("MMMM");
            String month1 = format.format(new Date());

            format = new SimpleDateFormat("yyyy");
            String year1 = format.format(new Date());

            try {
                String sql = "insert into invoices (invoice_id , employee_name , total , day , month, year , date) values (?,?,?,?,?,?,?)";
                pst = con.prepareStatement(sql);

                pst.setString(1, search_txt.getText());
                pst.setString(2, employeeName.getText());
                pst.setString(3, total_txt.getText());
                pst.setString(4, day1);
                pst.setString(5, month1);
                pst.setString(6, year1);
                pst.setString(7, date);

                pst.execute();

                JOptionPane.showMessageDialog(null, "saved invoice");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            } finally {
                try {
                    pst.close();
                } catch (SQLException ex) {
                    Logger.getLogger(EmployeesWork.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        if (model.getRowCount() == 0) {
            JOptionPane.showMessageDialog(null, "حدد الفاتورة ..");
        } else {
            jTabbedPane1.setSelectedIndex(0);

            double total = Double.parseDouble(total_txt.getText());
            double all_total = Double.parseDouble(all_txt.getText());

            Calendar cal = new GregorianCalendar();
            int year = cal.get(Calendar.YEAR);
            int month = cal.get(Calendar.MONTH);
            int day = cal.get(Calendar.DAY_OF_MONTH);

            String date = "" + day + "/" + (month + 1) + "/" + year + "";

            SimpleDateFormat format = new SimpleDateFormat("h:mm a");
            String time = format.format(new Date());

            try {
                textArea.setText(null);

                String sql = "select quantity , foodType , total from foodInfo where invoice_id = ? and date = ?";
                pst = con.prepareStatement(sql);
                pst.setString(1, search_txt.getText());
                pst.setString(2, date);

                rs = pst.executeQuery();

                textArea.append("\n                                   The Best Food\n\n" + "------------------------------------------------\n");
                textArea.append("          table number: " + desk_txt.getText() + "    \t    employee name :" + employeeName.getText() + "\n"
                        + "          invoice number: " + search_txt.getText() + "\t    date :" + date + "\n          time: " + time + "\n");
                textArea.append("------------------------------------------------\n");
                textArea.append("                 Qty                      item                      price\n");
                textArea.append("------------------------------------------------\n");

                while (rs.next()) {
                    String quantity = rs.getString("quantity");
                    String food_type = rs.getString("foodType");
                    String sum = rs.getString("total");
                    textArea.append("" + quantity + "                 " + food_type + " \t" + sum + "\n\n");

                }
                textArea.append("------------------------------------------------\n");
                textArea.append("          Subtotal\t" + total_txt.getText() + "\n");
                textArea.append("------------------------------------------------\n");
                textArea.append("          Total\t" + all_txt.getText());

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            } finally {
                try {
                    pst.close();
                    rs.close();
                } catch (SQLException ex) {
                    Logger.getLogger(EmplooyeLogin.class.getName()).log(Level.SEVERE, null, ex);
                }

            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

