    private void search_txtKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_search_txtKeyPressed
        DefaultTableModel model = (DefaultTableModel) table.getModel();

        Calendar cal = new GregorianCalendar();
        int year = cal.get(Calendar.YEAR);
        int month = cal.get(Calendar.MONTH);
        int day = cal.get(Calendar.DAY_OF_MONTH);

        String date = "" + day + "/" + (month + 1) + "/" + year + "";

        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            model.setRowCount(0);
            try {
                String sql = "select * from foodInfo where invoice_id = ? and date = ?";
                pst = con.prepareStatement(sql);
                pst.setString(1, search_txt.getText());
                pst.setString(2, date);
                rs = pst.executeQuery();

                Vector<Object> rows;

                while (rs.next()) {

                    rows = new Vector<Object>();

                    rows.add(rs.getString("foodType"));
                    rows.add(rs.getString("quantity"));
                    rows.add(rs.getString("price"));
                    rows.add(rs.getString("total"));

                    model.addRow(rows);

                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            } finally {
                try {
                    pst.close();
                    rs.close();
                } catch (SQLException ex) {
                    Logger.getLogger(EmplooyeLogin.class.getName()).log(Level.SEVERE, null, ex);
                }

            }
            try {
                String sql = "select sum(total) from foodInfo where invoice_id = ? and date = ? ";
                pst = con.prepareStatement(sql);
                pst.setString(1, search_txt.getText());
                pst.setString(2, date);
                rs = pst.executeQuery();

                if (rs.next()) {
                    String sum = rs.getString("sum(total)");
                    total_txt.setText(sum);
                    all_txt.setText(sum);
                    cash_txt.setText(sum);

                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            } finally {
                try {
                    pst.close();
                    rs.close();
                } catch (SQLException ex) {
                    Logger.getLogger(EmplooyeLogin.class.getName()).log(Level.SEVERE, null, ex);
                }

            }
        }
    }//GEN-LAST:event_search_txtKeyPressed

