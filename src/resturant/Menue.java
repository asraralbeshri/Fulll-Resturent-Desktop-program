    private int invoice_id() {

        Calendar cal = new GregorianCalendar();
        int day1 = cal.get(Calendar.DAY_OF_MONTH);
        int month2 = cal.get(Calendar.MONTH);
        int year1 = cal.get(Calendar.YEAR);

        String date1 = "" + day1 + "/" + (month2 + 1) + "/" + year1 + "";

        int result = 0;
        try {
            String sql = "select invoice_id from foodInfo where date = '" + date1 + "' order by invoice_id desc limit 1 ";
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            if (rs.next()) {
                result = rs.getInt("invoice_id");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } finally {
            try {
                pst.close();
                rs.close();
            } catch (SQLException ex) {
                Logger.getLogger(EmployeesWork.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        return result;
    }

