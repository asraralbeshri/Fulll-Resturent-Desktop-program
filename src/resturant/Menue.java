    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        if (model.getRowCount() == 0) {
            JOptionPane.showMessageDialog(null, "قم بالطلب اولا ثم أكد طلبك  ..");
        } else {
            int p = JOptionPane.showConfirmDialog(null, "هل آنت متأكد من تنفيذ طلبك ؟", "تأكيد الطلب", JOptionPane.YES_NO_OPTION);
            if (p == 0) {
                invoice_id = invoice_id() + 1;
                String food_type;
                String quantity;
                double price;
                double total1;
                // لتحديد اليوم ـــــــــــــــــــــــــــــــــــــ
                SimpleDateFormat date = new SimpleDateFormat("EEEE");
                String day = date.format(new Date());
                // لتحديد رقم الاسبوع في الشهر  ـــــــــــــــــــــــــــــــــــــ
                Calendar date_week = new GregorianCalendar();
                int week = date_week.get(Calendar.WEEK_OF_MONTH);
                // لتحديد الشهر  ـــــــــــــــــــــــــــــــــــــ
                SimpleDateFormat date_mounth = new SimpleDateFormat("MMMM");
                String mounth = date_mounth.format(new Date());
                //ـــــــــــــــــــــــــــــــــــــــــــــــــــــ
                SimpleDateFormat date_year = new SimpleDateFormat("yyyy");
                String year = date_year.format(new Date());
                //_____________________________________________________
                Calendar cal = new GregorianCalendar();
                int day1 = cal.get(Calendar.DAY_OF_MONTH);
                int month2 = cal.get(Calendar.MONTH);
                int year1 = cal.get(Calendar.YEAR);

                String date1 = "" + day1 + "/" + (month2 + 1) + "/" + year1 + "";

                int rows = table.getRowCount();
                for (int row = 0; row < rows; row++) {
                    try {

                        food_type = (String) table.getValueAt(row, 3);

                        quantity = table.getValueAt(row, 2).toString();

                        price = Double.parseDouble(table.getValueAt(row, 1).toString());

                        total1 = Double.parseDouble(table.getValueAt(row, 0).toString());

                        String sql = "insert into foodInfo (invoice_id , foodType , quantity , price , total , day , week , month , date,year) values (?,?,?,?,?,?,?,?,?,?)";
                        pst = con.prepareStatement(sql);

                        pst.setString(1, invoice_id + "");
                        pst.setString(2, food_type);
                        pst.setString(3, quantity);
                        pst.setString(4, price + "");
                        pst.setString(5, total1 + "");
                        pst.setString(6, day);
                        pst.setString(7, week + "");
                        pst.setString(8, mounth);
                        pst.setString(9, date1);
                        pst.setString(10, year);

                        pst.execute();

                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, e);
                    }

                }
                JOptionPane.showMessageDialog(null, "Your invoice number is = " + invoice_id);

                DefaultTableModel model1 = (DefaultTableModel) table_eats.getModel();
                model.setRowCount(0);
                model1.setRowCount(0);

                total = 0;
                total_txt3.setText("0.00 ر.س");
                txt_now.setText("0.00 ر.س");
                txt_all.setText("0.00 ر.س");
                total2_txt.setText("0.00 ر.س");
                all2_txt.setText("0.00 ر.س");
                total3_txt.setText("0.00 ر.س");
                all3_txt.setText("0.00 ر.س");
                cash_txt.setText("0.00 ر.س");

                jTabbedPane2.setSelectedIndex(1);
                jPanel18.setVisible(true);
                jPanel19.setVisible(true);
                this.setSize(1210, 750);
                this.setLocationRelativeTo(null);
                try {
                    pst.close();
                } catch (SQLException ex) {
                    Logger.getLogger(Menue.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

