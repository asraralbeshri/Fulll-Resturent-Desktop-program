    private void email_txtKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_email_txtKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            String Email_Pattern = "^[a-zA-Z0-9.-_]{1,20}@[a-zA-Z0-9]{1,20}.[a-zA-Z]{2,3}$";
            Pattern pattern = Pattern.compile(Email_Pattern);
            Matcher regexMatcher = pattern.matcher(email_txt.getText());
            if (!regexMatcher.matches()) {
                error1.setText("تأكد من كتابة البريد الالكتروني بالشكل الصحيح");
            } else {
                password_txt.requestFocusInWindow();
            }
        }
    }//GEN-LAST:event_email_txtKeyPressed

