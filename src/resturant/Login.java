    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
            String sql = "select * from userInfo where email = ? and password = ?";
            try {
                pst = con.prepareStatement(sql);
                pst.setString(1, email_txt.getText());
                pst.setString(2, password_txt.getText());

                rs = pst.executeQuery();

                if (rs.next()) {

                    User u = new User();

                    String name = rs.getString("Fname");
                    User.name_txt.setText(name);
                    String age = rs.getString("age");
                    User.age_txt.setText(age);
                    String cal = rs.getString("calories");
                    User.cal_txt.setText(cal);
                    String gender = rs.getString("gender");
                    User.gender_txt.setText(gender);

                    pst.close();
                    rs.close();

                    this.dispose();
                    u.setVisible(true);
                } else {
                    error.setText("تأكد من صحة البربد الالكتروني أو كلمة المرور ..");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            } finally {
                try {
                    pst.close();
                    rs.close();
                } catch (SQLException ex) {
                    Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
                }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

